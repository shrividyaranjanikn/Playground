// Variable declaration and initialization

// Using let keyword
let a = 10;
let b = 11;
let c = a+b;
console.log('The value of c is:' + c);

// Using var keyword
var d = 12;
var e = 2;
var sum = d+e;
console.log('Sum = '+sum);

// Concatinating two strings
let firstName = 'Shrividya';
